/* screen 객체로 화면의 너비와 높이 알아내기 */
'use strict'

onload = () => {
    document.writeln(`<p><b>screen.availWidth:</b> ${screen.availWidth}</p>`)
    document.writeln(`<p><b>screen.availHeight:</b> ${screen.availHeight}</p>`)
    document.writeln(`<p><b>screen.width:</b> ${screen.width}</p>`)
    document.writeln(`<p><b>screen.height:</b> ${screen.height}</p>`)
}
